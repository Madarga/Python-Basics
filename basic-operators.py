def add(a, b):
    return a+b

print('add(1, 2) ', add(1,2))

def multiply(a, b):
    return a*b

print('multiply(1, 2)', multiply(1,2))

def division(a, b):
    return a/b

print('division(1, 2)', division(1, 2))

def reminder(a, b):
    return a%b

print('modulo(1, 2)', modulo(1, 2))

def exponent(a, b):
    return a**b

print('exponent(1, 2)', exponent(1, 2))

def floor_division(a, b):
    return a//b

print('floor_division(1, 2)', floor_division(1, 2))

def is_identity(a, b):
    return a is b

print('is_identity(1, 2)', is_identity(1, 2))

def is_not_identity(a, b):
    return a is not b

print('is_not_identity(1, 2)', is_not_identity(1, 2))

def bitwise_and(a, b):
    return a & b

print('bitwise_and(1, 2)', bitwise_and(1, 2))

def bitwise_left_shift(a, b):
    return a >> b

print('bitwise_left_shift(1, 2)', bitwise_left_shift(1, 2))
